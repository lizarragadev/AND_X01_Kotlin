 * Ejemplo básico de creación de variables.
 * VAR : Variable mutable.
 * VAL : Variable inmutable.
 *
 * */

fun main() {
    val valor1 = 4
    val valor2 = 3
    println("Resultado suma: ${valor1.plus(valor2)}")
    println("Resultado resta: ${valor1.minus(valor2)}")
    println("Resultado multiplicación: ${valor1.times(valor2)}")
    println("Resultado división: ${valor1.div(valor2)}")
}
